<template>
    <div class="wrapper" :style="loading">
        <div class="spinner">
            <div class="rect1"></div>
            <div class="rect2"></div>
            <div class="rect3"></div>
            <div class="rect4"></div>
            <div class="rect5"></div>
        </div>
    </div>


</template>

<script>
    import Bus from './bus.js';

    export default {
        data() {
            return {
                loading : {
                    display : 'block'
                }
            }
        },

        created() {
            Bus.$on('loading', (status) => {
                if (status){
                    this.loading = {
                        display : 'block'
                    }
                }else {
                    this.loading = {
                        display : 'none'
                    }
                }

            });
        }
    }
</script>

